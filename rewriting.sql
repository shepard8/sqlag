CREATE VIEW v_atom_rew_order AS
SELECT qry_id, atm_id, row_number() OVER (PARTITION BY qry_id ORDER BY atm_stratum, atm_id) AS atm_rew_order
FROM v_query_rewritable
NATURAL JOIN v_atom_stratum;

